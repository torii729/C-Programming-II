#include <stdio.h>

void phone();
void graph(int cost[], int size);

// 기능명: main. 프로그램 시작점
// 내용: phone 함수를 호출하여 요금 데이터를 초기화하고 그래프를 출력한다.
// 입력: 없음
// 출력: 1~12월의 핸드폰 요금 그래프를 콘솔에 출력한다.
// 오류: 없음
int main()
{
	phone();
	return 0;
}

// 기능명: phone. 배열 초기화 및 그래프 호출
// 내용: 12개월의 핸드폰 요금을 배열에 저장하고,
//       graph 함수를 호출하여 각 월별 요금을 시각적으로 출력한다.
// 입력: 없음
// 출력: 각 월별 요금 그래프를 콘솔에 출력한다.
// 오류: 없음
void phone()
{
	int cost[12] = { 36000, 42000, 38000, 37000, 40000, 41000, 35000, 50000, 51000, 45000, 42000, 65000 };
	int size = sizeof(cost) / sizeof(cost[0]);

	graph(cost, size);
}

// 기능명: graph. 핸드폰 요금 막대 그래프 출력
// 내용: 배열에 저장된 요금을 2000원 단위로 나누어 '*' 문자로 표시하고,
//       월별 요금과 함께 막대 그래프 형태로 출력한다.
// 입력: int cost[] (요금 배열), int size (배열 크기)
// 출력: 월별 요금과 해당 요금에 대응하는 막대 그래프를 콘솔에 출력한다.
// 오류: 없음
void graph(int cost[], int size)
{
	for (int i = 0; i < size; i++)
	{
		int star = cost[i] / 2000; // 2000원당 '*' 한 개
		int index = i + 1;

		printf("%2d월 %6d : ", index, cost[i]);

		for (int a = 0; a < star; a++)
			printf("*");

		printf("\n");
	}
}
