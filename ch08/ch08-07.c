/*
    관련 개념:
    포인터는 변수의 메모리 주소를 저장하는 자료형이다.
    포인터 변수를 선언할 때 *을 사용하며, & 연산자로 변수의 주소를 구할 수 있다.
    잘못된 주소를 직접 대입하면 실행 오류가 발생할 수 있으므로,
    주소가 없을 때는 0이나 NULL로 초기화하는 것이 안전하다.
*/

#include <stdio.h>

// 기능명: main. 프로그램 시작점
// 내용: 포인터 변수의 선언, 초기화, 주소 출력 방법을 설명한다.
// 입력: 없음
// 출력: 포인터 값 (주소)
// 오류: 잘못된 주소 초기화 시 실행 오류 가능

int main(void)
{
    // int *p1 = 0x12345678; // 컴파일 에러
    int* p2 = (int*)0x12345678; // 실행 에러 가능

    int a = 10; // 정수 변수 선언 및 초기화
    int* p3 = &a; // a의 주소 저장

    int* p4 = 0; // 주소가 없으면 0으로 초기화
    int* p5 = NULL; // NULL 사용 가능

    printf("p2 = %p\n", p2); // p2 출력
    printf("p3 = %p\n", p3); // p3 출력
    printf("p4 = %p\n", p4); // p4 출력
    printf("p5 = %p\n", p5); // p5 출력

    return 0; // 정상 종료
}
