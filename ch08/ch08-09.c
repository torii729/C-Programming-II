#include <stdio.h>

/*
    관련 개념 :
    함수의 매개변수는 기본적으로 값에 의한 전달(Call by Value)이므로
    함수 내부에서 매개변수를 변경해도 원본 변수는 바뀌지 않는다.
    원본 값을 직접 변경하려면 포인터를 사용해야 한다.
*/

// 기능명: test1. 지역 변수 값 변경 시도
// 내용: main 함수에서 전달받은 값을 복사하여 사용. 원본 변수에는 영향 없음
// 입력: 정수 x (값 전달)
// 출력: 없음
// 오류: 없음
void test1(int x)
{
    x = 20; // 지역 변수 x를 변경 (main의 x는 변경되지 않음)
}

// 기능명: test2. 포인터로 원본 변수 값 변경
// 내용: main 함수의 변수 주소를 전달받아 직접 값을 변경
// 입력: 정수 포인터 p (주소 전달)
// 출력: 없음
// 오류: p가 NULL이면 접근 시 에러 발생 가능
void test2(int* p)
{
    *p = 20; // 포인터가 가리키는 main의 변수 값을 변경
}

// 기능명: main. 프로그램 실행 시작점
// 내용: test1과 test2 함수를 호출해 값 전달과 주소 전달의 차이를 확인
// 입력: 없음
// 출력: 각 함수 호출 후 변수 값 출력
// 오류: 없음
int main(void)
{
    int x = 10; // 정수 변수 선언 및 초기화

    test1(x); // x의 값 복사 전달
    printf("test1 호출 후 x = %d\n", x); // x는 여전히 10

    test2(&x); // x의 주소 전달
    printf("test2 호출 후 x = %d\n", x); // x가 20으로 변경됨

    return 0;
}
