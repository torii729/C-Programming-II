/*
    관련 개념:
    배열의 이름은 배열의 시작 주소를 의미한다.
    포인터에 증감 연산자를 적용하면 가리키는 원소가 순차적으로 바뀐다.
    *p는 현재 포인터가 가리키는 원소 값을 의미한다.

    메모리 동작:
    p는 arr[0]의 주소를 저장하고, 반복문마다 p++로 다음 원소를 가리킨다.
    따라서 주소와 그 위치의 값이 순서대로 출력된다.
*/

#include <stdio.h>

// 기능명: main. 배열 포인터 증감 연산 확인
// 내용: 배열 원소를 포인터로 가리킨 뒤, p++ 연산으로 이동하며 주소와 값을 출력한다.
// 입력: 없음
// 출력: 각 원소의 주소와 값
// 오류: 없음
int main(void)
{
    int arr[5] = { 1, 2, 3, 4, 5 };
    int* p = &arr[0]; // arr[0]의 주소 저장
    int i;

    for (i = 0; i < 5; i++, p++) // p는 arr[0]~arr[4]까지 이동
    {
        printf("p = %p, ", p); // 현재 포인터의 주소 출력
        printf("*p = %d\n", *p); // 포인터가 가리키는 값 출력
    }

    return 0; // 정상 종료
}
