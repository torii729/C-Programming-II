/*
    관련 개념 :
    함수의 매개변수는 값에 의한 전달(pass by value)이므로 일반 변수만 넘기면 원본은 바뀌지 않는다.
    그러나 포인터를 매개변수로 사용하면 원본 변수의 주소를 넘겨 직접 수정할 수 있다.
    swap 함수는 두 변수의 값을 교환한다.

    메모리 동작 :
    &a, &b를 인자로 넘기면 swap 함수 내부의 포인터 x, y는 각각 a와 b의 주소를 가리킨다.
    *x, *y 연산을 통해 a와 b의 실제 값을 변경할 수 있다.
*/

#include <stdio.h>

void swap(int* x, int* y);

// 기능명: main. swap 함수 호출
// 내용: 두 정수 a, b를 선언하고 swap 함수를 호출하여 값이 교환됨을 확인한다.
// 입력: 없음
// 출력: swap 전과 후의 a, b 값
// 오류: 없음
int main(void)
{
    int a = 3, b = 7;

    printf("a = %d, b = %d\n", a, b);
    swap(&a, &b); // 포인터에 의한 전달
    printf("a = %d, b = %d\n", a, b);

    return 0; // 정상 종료
}

// 기능명: swap. 두 정수 값 교환
// 내용: 두 정수의 주소를 받아 값을 서로 교환한다.
// 입력: int* x, int* y (두 정수의 주소)
// 출력: 없음 (원본 값이 직접 변경됨)
// 오류: 전달된 포인터가 NULL일 경우 동작 보장되지 않음
void swap(int* x, int* y)
{
    int temp = *x; // x가 가리키는 값을 temp에 저장
    *x = *y; // y가 가리키는 값을 x가 가리키는 변수에 저장
    *y = temp; // temp 값을 y가 가리키는 변수에 저장
}
