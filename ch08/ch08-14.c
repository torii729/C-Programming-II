/*
    관련 개념:
    배열의 이름, 배열의 시작 주소, &arr[0]은 모두 동일하다.
    포인터 p를 배열 이름처럼 사용할 수 있다.
    p[i]는 *(p+i)와 같은 의미이다.

    메모리 동작:
    arr 배열의 시작 주소가 p에 저장된다.
    for문에서 p[i]로 각 원소를 배열처럼 접근한다.
*/

#include <stdio.h>

// 기능명: main. 포인터를 배열처럼 사용
// 내용: 포인터 p에 배열 arr의 시작 주소를 저장하고, p[i]로 원소를 출력한다.
// 입력: 없음
// 출력: arr 배열의 각 원소 (p[i] 사용)
// 오류: 없음
int main(void)
{
    int arr[5] = { 1, 2, 3, 4, 5 }; // 정수 배열 선언 및 초기화
    int* p = arr; // 배열의 시작 주소를 포인터 p에 저장
    int i;

    for (i = 0; i < 5; i++)
        printf("p[%d] = %d\n", i, p[i]); // p를 배열 이름처럼 사용

    return 0; // 정상 종료
}


