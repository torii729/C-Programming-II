/*
    관련 개념 :
    포인터 배열은 여러 개의 주소를 원소로 가지는 배열이다.
    int *arr[5]는 int 변수 5개의 주소를 저장할 수 있다.
    arr[i]는 int* 타입이므로 *arr[i]로 실제 값을 접근할 수 있다.

    메모리 동작 :
    변수 a~e는 각각 독립적인 int 공간을 가진다.
    arr 배열은 이 변수들의 주소를 저장한다.
    반복문에서 *arr[i] = i는 해당 변수를 i 값으로 초기화한다.
*/

#include <stdio.h>

// 기능명: main. 포인터 배열의 선언과 사용
// 내용: 다섯 개 변수의 주소를 포인터 배열에 저장하고 반복문으로 값을 초기화 및 출력한다.
// 입력: 없음
// 출력: 0 1 2 3 4
// 오류: 없음
int main(void)
{
    int a, b, c, d, e;
    int* arr[5] = { &a, &b, &c, &d, &e }; // 포인터 배열
    int i;

    for (i = 0; i < 5; i++) {
        *arr[i] = i;
        printf("%d ", *arr[i]); // 포인터 배열을 통한 값 출력
    }
    printf("\n");

    return 0; // 정상 종료
}

