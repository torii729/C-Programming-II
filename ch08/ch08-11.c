/*
    관련 개념:
    포인터에 정수를 더하면 가리키는 자료형의 크기만큼 주소가 이동한다.
    int 포인터는 4바이트씩 이동한다.
    double 포인터는 8바이트씩 이동한다.
    char 포인터는 1바이트씩 이동한다.
    %p 서식은 주소를 출력한다.

    메모리 동작:
    p q r는 같은 기준 주소를 서로 다른 자료형 포인터로 해석한다.
    p+1 p+2 같은 표현은 다음 원소 위치의 주소를 계산한다.
*/

#include <stdio.h>

// 기능명: main. 포인터와 정수의 덧셈 결과 출력
// 내용: int double char 포인터에 정수를 더했을 때 주소가 어떻게 변하는지 출력한다.
// 입력: 없음
// 출력: 각 포인터와 포인터 산술 결과의 주소
// 오류: 임의의 절대 주소를 사용하므로 역참조는 하지 않는다.
int main(void)
{
    int* p = (int*)0x100; // 예시 절대 주소
    double* q = (double*)0x100; // 같은 기준 주소
    char* r = (char*)0x100; // 같은 기준 주소

    printf("int*   : %p, %p, %p\n", (void*)p, (void*)(p + 1), (void*)(p + 2)); // 4바이트 간격
    printf("double*: %p, %p, %p\n", (void*)q, (void*)(q + 1), (void*)(q + 2)); // 8바이트 간격
    printf("char*  : %p, %p, %p\n", (void*)r, (void*)(r + 1), (void*)(r + 2)); // 1바이트 간격

    return 0; // 정상 종료
}
