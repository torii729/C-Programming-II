/*
    관련 개념:
    포인터(pointer)는 변수의 주소를 저장하는 변수이다.
    &x는 변수 x의 주소를 구하는 연산자이며, *p는 포인터가 가리키는 실제 값을 의미한다.
    포인터를 통해 원본 변수 값을 간접적으로 변경할 수 있다.

    메모리 동작:
    int *p = &x; 는 p에 x의 주소를 저장한다.
    *p에 값을 대입하면, 그 값은 실제 x에 저장된다.
*/

#include <stdio.h>

// 기능명: main. 프로그램 실행 시작점
// 내용: 정수 변수와 포인터를 선언하고 주소와 값을 출력하며 포인터를 통한 값 변경 확인
// 입력: 없음
// 출력: 변수와 포인터의 값 및 주소 출력
// 오류: 없음
int main(void)
{
    int x = 10; // 정수 변수 x 선언 및 초기화
    int* p = &x; // p는 x의 주소로 초기화

    printf("x = %d\n", x); // x 값 출력
    printf("&x = %p\n", &x); // x의 주소 출력

    printf("p = %p\n", p); // 포인터 p에 저장된 값 (x의 주소)
    printf("*p = %d\n", *p); // 포인터 p가 가리키는 값 (x의 값)
    printf("&p = %p\n", &p); // 포인터 변수 p의 주소

    *p = 20; // 포인터 p를 통해 x 값을 20으로 변경
    printf("*p = %d\n", *p); // 변경된 x 값 출력 (20)

    return 0;
}
