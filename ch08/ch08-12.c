/*
    관련 개념:
    포인터 연산에서 p+i는 배열의 i번째 원소의 주소를 가리킨다.
    *(p+i)는 배열의 i번째 원소 값을 의미한다.
    따라서 배열의 인덱스를 이용한 접근 arr[i]와 동일하다.
*/

// 기능명: main. 프로그램 시작점
// 내용: 배열의 원소 주소와 값을 포인터 연산으로 출력한다.
// 입력: 없음
// 출력: 배열 원소의 주소와 값
// 오류: 없음
#include <stdio.h>

int main(void)
{
    int arr[5] = { 1, 2, 3, 4, 5 }; // 배열 초기화
    int* p = &arr[0]; // arr[0]의 주소를 포인터 p에 저장
    int i;

    for (i = 0; i < 5; i++)
    {
        printf("p + %d = %p, ", i, p + i); // 배열 원소의 주소 출력
        printf("*(p + %d) = %d\n", i, *(p + i)); // 배열 원소의 값 출력
    }

    return 0;
}
