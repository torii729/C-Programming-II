/*
    관련 개념:
    배열 이름은 상수 포인터처럼 동작하여 다른 배열로 대입할 수 없다.
    그러나 일반 포인터 변수는 가리키는 대상을 변경할 수 있다.
    배열 원소 복사는 반복문을 통해 하나씩 대입해야 한다.

    메모리 동작:
    int x[5]는 고정된 메모리 공간에 5개 정수를 저장한다.
    int *p는 처음에 x[0]을 가리키다가 이후 y[0]을 가리키도록 변경된다.
    p[i]는 배열 원소 접근처럼 동작하며, *p와 p++로도 순회할 수 있다.
*/

#include <stdio.h>

// 기능명: main. 배열과 포인터의 차이 출력
// 내용: 포인터가 배열 원소를 가리키는 방식과 배열 복사 시 동작 차이를 출력한다.
// 입력: 없음
// 출력: 배열 x와 배열 y의 값 출력
// 오류: 없음
int main(void)
{
    int x[5] = { 1, 2, 3, 4, 5 }; // 배열 x 초기화
    int y[5]; // 배열 y
    int* p = x; // p는 x[0]을 가리킴
    int i;

    for (i = 0; i < 5; i++)
        printf("%d ", p[i]); // 포인터를 배열처럼 사용
    printf("\n");

    p = y; // 이제 p는 y[0]을 가리킴
    for (i = 0; i < 5; i++)
        p[i] = x[i]; // y 배열에 x 배열 원소 복사

    for (i = 0; i < 5; i++, p++)
        printf("%d ", *p); // p가 가리키는 원소 출력
    printf("\n");

    return 0; // 정상 종료
}
