/*
    관련 개념:
    매크로 상수(#define)를 사용하면 배열의 크기를 코드 여러 곳에서 반복하지 않고 하나의 이름으로 관리할 수 있다.
    배열의 크기를 변경할 때 매크로 정의만 수정하면 전체 코드가 자동으로 반영된다.
    for 반복문으로 배열의 모든 원소를 초기화하거나 출력할 수 있다.

    메모리 동작:
    int arr[ARR_SIZE]는 스택에 ARR_SIZE 개의 int 공간을 연속적으로 할당한다.
    arr[i]는 배열의 i번째 원소에 접근한다.

    이 방식을 쓰는 이유:
    배열 크기를 코드에 직접 숫자로 쓰는 것보다 매크로 상수를 사용하면 유지보수가 편리하다.
*/

#include <stdio.h>

#define ARR_SIZE 5 // 배열 크기를 나타내는 매크로 상수 정의

// 기능명: main. 매크로 상수를 이용한 배열 선언 및 출력
// 내용: ARR_SIZE 매크로를 이용해 배열 크기를 정의하고,
//       for 반복문으로 배열을 초기화 및 출력한다.
// 입력: 없음
// 출력: 배열의 모든 원소를 콘솔에 출력한다.
// 오류: 없음
int main(void)
{
    int arr[ARR_SIZE]; // 배열 크기를 매크로로 지정
    int i;

    for (i = 0; i < ARR_SIZE; i++)
        arr[i] = 0; // 배열 모든 원소를 0으로 초기화

    printf("arr = ");
    for (i = 0; i < ARR_SIZE; i++)
        printf("%d ", arr[i]); // 배열 원소 출력
    printf("\n");

    return 0;
}
