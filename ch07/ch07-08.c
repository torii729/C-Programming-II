/*
    관련 개념:
    배열 초기화 시 크기보다 적은 개수의 값만 지정하면 나머지는 자동으로 0으로 초기화된다.
    {0}으로 초기화하면 배열 전체가 0으로 채워진다.
    for 반복문을 이용하여 배열의 모든 원소를 순서대로 출력할 수 있다.

    메모리 동작:
    int amount[5] = {1,1,5}; -> arr[0]=1, arr[1]=1, arr[2]=5, 나머지 arr[3], arr[4]는 0으로 채워진다.
    int price[5] = {0}; -> 배열 전체가 0으로 초기화된다.

    이 방식을 쓰는 이유:
    배열의 초기값을 명확히 지정하거나 전체를 0으로 세팅할 때 간결하게 작성할 수 있다.
*/

#include <stdio.h>

// 기능명: main. 배열 부분 초기화 및 출력
// 내용: 정수 배열을 부분적으로 초기화하거나 {0}으로 전체 초기화한 뒤,
//       각 배열의 모든 원소를 순서대로 출력한다.
// 입력: 없음
// 출력: amount 배열과 price 배열의 원소를 콘솔에 출력한다.
// 오류: 없음
int main(void)
{
    int amount[5] = { 1, 1, 5 }; // 일부 원소만 지정, 나머지는 0으로 초기화
    int price[5] = { 0 }; // 배열 전체를 0으로 초기화
    int i;

    printf("amount = ");
    for (i = 0; i < 5; i++)
        printf("%d ", amount[i]); // amount 배열 출력
    printf("\n");

    printf("price = ");
    for (i = 0; i < 5; i++)
        printf("%d ", price[i]); // price 배열 출력
    printf("\n");

    return 0;
}
