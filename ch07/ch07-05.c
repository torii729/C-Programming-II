/*
    관련 개념 :
    sizeof 연산자는 배열 전체가 차지하는 바이트 크기를 반환한다.
    배열의 크기(원소 개수)는 전체 바이트 크기를 한 원소의 크기로 나누어 구할 수 있다.
    for 반복문을 이용하면 배열의 모든 원소를 원하는 값으로 초기화할 수 있다.

    메모리 동작 :
    int arr[5]는 스택에 5개의 int 공간을 연속적으로 할당한다.
    sizeof(arr)는 배열 전체 크기(바이트 단위)를 계산한다.
    sizeof(arr[0])는 배열의 첫 원소 크기를 계산한다.

    이 방식을 쓰는 이유 :
    배열의 크기를 직접 상수로 쓰지 않고 sizeof로 계산하면 코드가 더 안전하고 유연해진다.
    자료형 크기가 달라져도 올바르게 배열 크기를 구할 수 있다.
*/

#include <stdio.h>

int main(void)
{
    int arr[5]; // 크기가 5인 int 배열
    int byte_size = 0; // 배열의 바이트 크기를 저장할 변수
    int size = 0; // 배열의 원소 개수를 저장할 변수
    int i;

    byte_size = sizeof(arr); // 배열 전체 바이트 크기
    printf("배열의 바이트 크기: %d\n", byte_size);

    size = sizeof(arr) / sizeof(arr[0]); // 배열 원소 개수
    printf("배열의 크기: %d\n", size);

    for (i = 0; i < size; i++)
        arr[i] = 0; // 배열 모든 원소를 0으로 초기화

    return 0;
}
