/*
    관련 개념:
    배열의 인덱스는 0부터 시작하여 (크기 - 1)까지만 유효하다.
    잘못된 인덱스를 사용하면 메모리의 다른 영역에 접근하게 되어
    정의되지 않은 동작(오류, 잘못된 값, 프로그램 비정상 종료)이 발생할 수 있다.

    메모리 동작:
    int arr[5]는 스택에 int 5개 공간(arr[0]~arr[4])만 할당된다.
    arr[5]는 배열 경계를 넘어선 주소를 참조하게 되며, 이는 안전하지 않다.
*/

#include <stdio.h>

// 기능명: main. 배열 인덱스 오류 시연
// 내용: 배열을 선언 및 초기화하고, 유효 인덱스 범위 내의 값을 출력한 뒤
//       잘못된 인덱스 접근이 어떤 문제를 일으키는지 시연한다.
// 입력: 없음
// 출력: 배열의 모든 원소와 잘못된 인덱스 접근 결과를 콘솔에 출력한다.
// 오류: 배열 경계를 벗어난 접근으로 정의되지 않은 동작 발생 가능
int main(void)
{
    int arr[5] = { 10, 20, 30, 40, 50 }; // 크기 5 배열 초기화
    int i;

    printf("arr = ");
    for (i = 0; i < 5; i++)
        printf("%d ", arr[i]); // 배열 원소 출력
    printf("\n");

    printf("arr[5] = %d\n", arr[5]); // 잘못된 인덱스 접근 (경고 또는 비정상 동작 가능)
    arr[5] = 100; // 배열 범위를 벗어난 위치에 값 저장 (정의되지 않은 동작)

    return 0;
}
