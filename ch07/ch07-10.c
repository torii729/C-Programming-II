/*
    관련 개념:
    배열의 인덱스는 0부터 시작하여 (크기 - 1)까지만 유효하다.
    잘못된 인덱스를 사용하면 메모리의 다른 영역에 접근하게 되어 정의되지 않은 동작(오류, 잘못된 값, 프로그램 비정상 종료)이 발생할 수 있다.

    메모리 동작:
    int arr[5]는 스택에 int 5개 공간(arr[0]~arr[4])만 할당된다.
    arr[5]는 배열 경계를 넘어선 주소를 참조하게 되며, 이는 안전하지 않다.
*/

#include <stdio.h>

int main(void)
{
    int arr[5] = { 10, 20, 30, 40, 50 }; // 크기 5 배열 초기화
    int i;

    printf("arr = ");
    for (i = 0; i < 5; i++)
        printf("%d ", arr[i]); // 배열 원소 출력
    printf("\n");

    printf("arr[5] = %d\n", arr[5]); // 잘못된 인덱스 접근, 실제로 함수 'main' 버퍼 'arr'이 오버런되며 4바이트가 오프셋 20부터 쓰인다는 문구가 출력된다.
    arr[5] = 100; // 잘못된 인덱스에 값 저장 (정의되지 않은 동작)

    return 0;
}
