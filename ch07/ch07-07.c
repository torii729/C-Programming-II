/*
    관련 개념:
    배열을 선언과 동시에 중괄호 초기화 리스트로 값을 지정할 수 있다.
    배열의 크기와 동일한 개수의 초기값을 주면 모든 원소가 지정된 값으로 채워진다.
    for 반복문을 사용하여 배열에 저장된 값을 순서대로 출력할 수 있다.

    메모리 동작:
    int arr[5]는 스택에 int 5개 공간을 연속적으로 할당한다.
    초기화 리스트 {1,2,3,4,5}가 각각 arr[0]~arr[4]에 복사된다.

    이 방식을 쓰는 이유:
    배열을 초기 상태에서 원하는 값으로 바로 세팅할 수 있어 코드가 간결해진다.
*/

#include <stdio.h>

// 기능명: main. 배열 선언 및 초기화
// 내용: 정수형 배열을 선언과 동시에 초기화 리스트로 초기화하고,
//       for 반복문을 이용해 배열의 모든 원소를 출력한다.
// 입력: 없음
// 출력: 배열의 모든 원소를 콘솔에 출력한다.
// 오류: 없음
int main(void)
{
    int arr[5] = { 1, 2, 3, 4, 5 }; // 배열을 선언과 동시에 초기화
    int i;

    printf("arr = ");
    for (i = 0; i < 5; i++)
        printf("%d ", arr[i]); // 배열 원소 출력
    printf("\n");

    return 0;
}
