/*
    관련 개념:
    배열 이름끼리 비교(x == y)는 배열의 주소를 비교하는 것이므로, 배열의 내용이 같아도 false가 된다.
    배열의 내용을 비교하려면 반복문을 통해 각 원소를 하나씩 비교해야 한다.
    is_equal 변수를 두어 모든 원소가 같은지를 판별할 수 있다.

    메모리 동작 :
    int x[5], y[5]는 각각 스택에 별도의 공간을 가진다.
    x == y 비교는 두 배열의 시작 주소가 같은지 검사한다.
    반복문에서는 x[i]와 y[i]를 직접 비교하여 실제 저장된 값을 확인한다.
*/

#include <stdio.h>

int main(void)
{
    int x[5] = { 10, 20, 30, 40, 50 }; // 첫 번째 배열
    int y[5] = { 10, 20, 30, 40, 50 }; // 두 번째 배열
    int i;
    int is_equal;

    if (x == y) // 배열 이름끼리 비교 → 주소 비교
        printf("두 배열의 주소가 같습니다.\n");

    is_equal = 1; // 배열의 내용이 같다고 가정한다

    for (i = 0; i < 5; i++) {
        if (x[i] != y[i]) { // 배열의 원소끼리 비교
            is_equal = 0; // 하나라도 다르면 false
            break; // 더 이상 비교할 필요 없음
        }
    }

    if (is_equal == 1) // 모든 원소가 같으면,
        printf("두 배열의 내용이 같습니다.\n");

    return 0;
}
